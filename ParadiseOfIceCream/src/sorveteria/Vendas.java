package sorveteria;

public class Vendas {
	private int cod;
	private int qntProdVend;
	private String horaVenda;
	private String nomeCliente;
	private String prodComprado;
	
	
	
	public int getCod() {
		return cod;
	}
	public void setCod(int cod) {
		this.cod = cod;
	}
	public int getQntProdVend() {
		
		return qntProdVend;
	}
	public void setQntProdVend(int qtdProdVend) {
		this.qntProdVend = qtdProdVend;
	}
	public String getHoraVenda() {
		return horaVenda;
	}
	public void setHoraVenda(String horaVenda) {
		this.horaVenda = horaVenda;
	}
	public String getNomeCliente() {
		return nomeCliente;
	}
	public void setNomeCliente(String nomeCliente) {
		this.nomeCliente = nomeCliente;
	}
	public String getProdComprado() {
		return prodComprado;
	}
	public void setProdComprado(String prodComprado) {
		this.prodComprado = prodComprado;
	}
	
	

}
